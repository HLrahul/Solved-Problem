class Solution {
  public:
    int setBits(int N) {
        bitset<32> binary(N); // Convert N to binary representation
        int count = binary.count(); // Count the number of set bits
        return count;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int N;
        cin >> N;

        Solution ob;
        int cnt = ob.setBits(N);
        cout << cnt << endl;
    }
    return 0;
}

